CREATE TABLE `exim_accounts` (
  `uid` int(11) NOT NULL AUTO_INCREMENT,
  `email_address` varchar(255) NOT NULL DEFAULT '',
  `user` varchar(255) NOT NULL DEFAULT '',
  `password` varchar(255) NOT NULL DEFAULT '',
  `domain` int(11) NOT NULL DEFAULT '0',
  `system_guid` int(11) NOT NULL DEFAULT '12',
  `system_uid` int(11) NOT NULL DEFAULT '8',
  `autoreply` tinyint(1) NOT NULL DEFAULT '0',
  `forward` tinyint(1) NOT NULL DEFAULT '0',
  `maildir` varchar(255) NOT NULL DEFAULT '',
  `home` varchar(255) NOT NULL DEFAULT '',
  `active` tinyint(1) NOT NULL DEFAULT '0',
  `deliver_first` tinyint(1) NOT NULL DEFAULT '0',
  PRIMARY KEY (`uid`),
  KEY `user` (`user`),
  KEY `password` (`password`),
  KEY `domain` (`domain`),
  KEY `autoreply` (`autoreply`),
  KEY `forward` (`forward`),
  KEY `active` (`active`)
) ENGINE=MyISAM DEFAULT CHARSET=latin1 ROW_FORMAT=DYNAMIC COMMENT='This table stores the local e-mail accounts data. '