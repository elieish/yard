CREATE TABLE `exim_logs` (
  `uid` bigint(20) NOT NULL AUTO_INCREMENT,
  `date` datetime DEFAULT '0000-00-00 00:00:00',
  `company` int(10) unsigned NOT NULL DEFAULT '0',
  `cost_center` int(10) unsigned NOT NULL DEFAULT '0',
  `department` int(10) unsigned NOT NULL DEFAULT '0',
  `sender` varchar(100) DEFAULT NULL,
  `fax_no` varchar(50) DEFAULT NULL,
  `message_id` varchar(100) DEFAULT NULL,
  `subject` varchar(255) DEFAULT NULL,
  `fax_reference` varchar(100) DEFAULT NULL,
  `status` varchar(100) DEFAULT 'Unknown',
  `status_code` varchar(10) DEFAULT NULL,
  `pages` smallint(5) unsigned DEFAULT '0',
  `transmit_time` smallint(5) unsigned DEFAULT '0',
  `date_processed` datetime DEFAULT '0000-00-00 00:00:00',
  `retries` tinyint(4) DEFAULT '0',
  `cost` decimal(10,2) NOT NULL DEFAULT '0.00',
  PRIMARY KEY (`uid`)
) ENGINE=MyISAM DEFAULT CHARSET=utf8